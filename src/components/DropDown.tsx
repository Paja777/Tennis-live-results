import * as React from 'react';
import Button from '@mui/material/Button';
import Menu from '@mui/material/Menu';
import MenuItem from '@mui/material/MenuItem';
import { useNavigate } from 'react-router';
import { useAppDispatch } from '../store/configureStore';
import { signOut } from '../features/account/accountSlice';

export default function DropDown({name}: any) {
    const navigate = useNavigate();
    const dispatch = useAppDispatch();
  const [anchorEl, setAnchorEl] = React.useState<null | HTMLElement>(null);
  const open = Boolean(anchorEl);
  const handleClick = (event: React.MouseEvent<HTMLElement>) => {
    setAnchorEl(event.currentTarget);
  };
  const handleClose = () => {
    setAnchorEl(null);
  };

  return (
    <div>
      <Button
        id="demo-positioned-button"
        aria-controls={open ? 'demo-positioned-menu' : undefined}
        aria-haspopup="true"
        aria-expanded={open ? 'true' : undefined}
        onClick={handleClick}
        sx={{mt:0, ml:0}}
      >
        {name}
      </Button>
      <Menu
        id="demo-positioned-menu"
        aria-labelledby="demo-positioned-button"
        anchorEl={anchorEl}
        open={open}
        onClose={handleClose}
        anchorOrigin={{
          vertical: 'top',
          horizontal: 'left',
        }}
        transformOrigin={{
          vertical: 'top',
          horizontal: 'left',
        }}
      >
        <MenuItem onClick={() => { navigate('/account'); handleClose();}}>Profile</MenuItem>
        <MenuItem onClick={() => {navigate('/listpage'); handleClose();}}>Matches list</MenuItem>
        <MenuItem onClick={() => { dispatch(signOut()); handleClose(); }}>Logout</MenuItem>
      </Menu>
    </div>
  );
}
